/* ==========================================================================
   DEAL LINKS — slug-aware URL helper
   The Pharma Closeout · thepharmacloseout.com/deals

   Maps deal_id -> pre-generated static SEO page (deals/<slug>.html) when the
   build-time slug map is available, otherwise falls back to the JS-rendered
   interactive page (deal.html?id=<deal_id>). The slug map is an optional
   build artifact (assets/deal-slugs.json) generated by a separate task —
   this module must not assume it exists.
   ========================================================================== */

let slugMap = {}

/** Load the deal_id -> slug map. Safe to call even if the file is missing
 *  or fails to parse — falls back to interactive-page links. */
export async function loadSlugMap(url = 'assets/deal-slugs.json') {
  try {
    const r = await fetch(url)
    if (r.ok) slugMap = await r.json()
  } catch {
    /* map optional - fallback links still work */
  }
  return slugMap
}

/** Resolve the href for a deal: static SEO page when its slug is known,
 *  otherwise the interactive page keyed by deal_id. */
export function dealUrl(dealId) {
  const slug = slugMap[dealId]
  return slug ? `deals/${slug}.html` : `deal.html?id=${encodeURIComponent(dealId)}`
}

/** Test-only seam for injecting a slug map without a network fetch. */
export function _setSlugMap(m) {
  slugMap = m
}
